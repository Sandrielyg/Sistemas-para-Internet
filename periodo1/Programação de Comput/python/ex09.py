numerodigitado = int(input("Digite um número entre 1 e 4. \n"))
if numerodigitado == 1:
  print ("Você ganhou uma revista!")
elif numerodigitado == 2:
  print ("Você ganhou um mês de jornal grátis!")
elif numerodigitado == 3:
  print ("Chicletes")
elif numerodigitado == 4:
  print ("Caça palavra")
else:
  print ("Opção inválida")
